package opreater;

public class Oper3 {
public static void main(String[] args) {
	boolean 참거짓=true;  //1
	참거짓=false;         //0
	      //0
	
	참거짓=3>2;
	
	System.out.println(참거짓);
	
	참거짓=3<2;
	
	System.out.println(참거짓);
	
	//=: 대입연산자 (오른쪽 값을 오니쪽에 넣는다)
	//수학적인 같다 ==  
	//다르다 !=
	
	참거짓=2==2;
	System.out.println(참거짓);
	참거짓=2!=2;
	System.out.println(참거짓);
}
}
