package opreater;

public class Oper4 {
public static void main(String[] args) {
	int x=20;
	int y=10;
	boolean re;
	
	re=x>y;
	System.out.println("x>y="+re);
	
	re=x<y;
	System.out.println("x<y="+re);
	
	re=x>=y;
	System.out.println("x>=y="+re);
	
	re=x==y;
	System.out.println("x==y="+re);
	
	re=x!=y;
	System.out.println("x!=y="+re);
}
}
