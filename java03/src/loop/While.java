package loop;

public class While {
public static void main(String[] args) {
	//제어문: 프로그램의 실행흐름을 변경하는 것
	//조건문: 
	//반복문 :While, for
	
	//while(){} :if 문과 유사 하나 한가지 차이 있음
	//if(){} :()조건이 맞으면 {} 실행
	//while(){} :()조건이 맞으면 {} 실행하고 다시 ()로 올라감
	
	if(true) {
		System.out.println("if문 실행");
	}
	
	while(true) {
		System.out.println("while문 실행");
	}
}
}
